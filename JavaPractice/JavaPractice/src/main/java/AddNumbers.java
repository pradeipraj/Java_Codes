public class AddNumbers {

    public static void main(String[] args){
        int a=10;
        int b=10;
        System.out.println("The Sum of a and b: " + (a+b));
        System.out.println("The Sub of a and b: " + (a-b));
        System.out.println("The Pro of a and b: " + (a*b));
        System.out.println("The Div of a and b: " + (a/b));
        System.out.println("The Per of a and b: " + (a%b));
    }
}
