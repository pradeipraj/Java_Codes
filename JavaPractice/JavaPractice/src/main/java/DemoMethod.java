public class DemoMethod {
    public static void main (String[] args)
    {
        myMethod();
        myMethod1();
    }
    static void myMethod()
    {
        System.out.println("My Method is executed");
        System.out.println("You can call me many times");
    }
    static void myMethod1()
    {
        System.out.println("This is my 2nd Method");
        System.out.println("You can multiple methods");
    }
}
